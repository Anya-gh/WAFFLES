package uk.ac.warwick.cs126.util;

public class ArrayCompare {

    public static boolean equals(Long[] array1, Long[] array2) {
        if (array1.length != array2.length) {
            return false;
        }
        for (int i = 0; i < array1.length; i++) {
            if (array1[i] != array2[i]) {
                return false;
            }
        }
        return true;
    }
}